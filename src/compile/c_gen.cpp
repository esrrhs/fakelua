static inline uint32_t FlHashString(const char *str, int len) {
    uint32_t hash = 5381;
    for (int i = 0; i < len; ++i) {
        hash = ((hash << 5) + hash) + (uint8_t)str[i];
    }
    if (hash == 0) {
        hash = 1;
    }
    return hash;
}

#define SET_STRING(v, s, str, len) do { \
