#include <stdbool.h>
#include <string.h>
#include <math.h>
#include <assert.h>

typedef struct VarString {
    int size_;
    uint32_t hash_;
    char data_[0];
} VarString;

typedef struct VarTable VarTable;

typedef struct CVar {
    int type_;
    int flag_;
    union {
        bool b;
        int64_t i;
        double f;
        VarString *s;
        VarTable *t;
    } data_;
} CVar;

typedef struct VarEntry {
    CVar key;
    CVar val;
    uint32_t hash;
} VarEntry;

typedef struct TableNode {
    VarEntry entry;
    uint32_t next;
    uint32_t active_pos;
} TableNode;

struct VarTable {
    uint32_t count_;
    uint32_t bucket_count_;
    TableNode *nodes_;
    uint32_t *active_list_;
    VarEntry quick_data_[8];
    uint32_t free_list_idx_;
};

typedef struct State State;

#define STR_SIZE(s) ((s)->size_)
#define STR_DATA(s) ((s)->data_)
#define TABLE_SIZE(t) ((t)->count_)

enum {
    VAR_NIL = 0,
    VAR_BOOL = 1,
    VAR_INT = 2,
    VAR_FLOAT = 3,
    VAR_STRING = 4,
    VAR_STRINGID = 5,
    VAR_TABLE = 6,
};

#define SET_NIL(v) do { (v).type_ = VAR_NIL; } while(0)
#define SET_BOOL(v, val) do { (v).type_ = VAR_BOOL; (v).data_.b = (val); } while(0)
#define SET_INT(v, val) do { (v).type_ = VAR_INT; (v).data_.i = (val); } while(0)
#define SET_FLOAT(v, val) do { \
    double __f = (val); \
    int64_t __i = (int64_t)__f; \
