#define OpDiv(a, b, res) do { \
    CVar _ra = (a); CVar _rb = (b); CheckNum(_ra); CheckNum(_rb); \
    double _f1 = (_ra.type_ == VAR_INT) ? (double)_ra.data_.i : _ra.data_.f; \
